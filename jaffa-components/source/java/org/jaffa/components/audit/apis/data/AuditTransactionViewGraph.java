package org.jaffa.components.audit.apis.data;

import org.jaffa.beans.factory.config.StaticContext;
import org.jaffa.datatypes.DateTime;
import org.jaffa.soa.graph.GraphDataObject;

/**
 * @author GautamJ
 */
public class AuditTransactionViewGraph extends GraphDataObject {

    private String transactionId;
    private String processName;
    private String subProcessName;
    private String reason;
    private String createdBy;
    private DateTime createdOn;
    private String objectName;
    private String changeType;
    private String fieldName;
    private String fromValue;
    private String toValue;
    private Boolean changed;
    private Boolean flex;
    private Boolean overflowFlag;
    private String multilineHtmlFlag;
    private String objectLabel;
    private String fieldLabel;

    /**
     * Default constructor.
     */
    public AuditTransactionViewGraph() {
        StaticContext.configure(this);
    }

    public String getTransactionId() {
        return transactionId;
    }

    public void setTransactionId(String transactionId) {
        this.transactionId = transactionId;
    }

    public String getProcessName() {
        return processName;
    }

    public void setProcessName(String processName) {
        this.processName = processName;
    }

    public String getSubProcessName() {
        return subProcessName;
    }

    public void setSubProcessName(String subProcessName) {
        this.subProcessName = subProcessName;
    }

    public String getReason() {
        return reason;
    }

    public void setReason(String reason) {
        this.reason = reason;
    }

    public String getCreatedBy() {
        return createdBy;
    }

    public void setCreatedBy(String createdBy) {
        this.createdBy = createdBy;
    }

    public DateTime getCreatedOn() {
        return createdOn;
    }

    public void setCreatedOn(DateTime createdOn) {
        this.createdOn = createdOn;
    }

    public String getObjectName() {
        return objectName;
    }

    public void setObjectName(String objectName) {
        this.objectName = objectName;
    }

    public String getChangeType() {
        return changeType;
    }

    public void setChangeType(String changeType) {
        this.changeType = changeType;
    }

    public String getFieldName() {
        return fieldName;
    }

    public void setFieldName(String fieldName) {
        this.fieldName = fieldName;
    }

    public String getFromValue() {
        return fromValue;
    }

    public void setFromValue(String fromValue) {
        this.fromValue = fromValue;
    }

    public String getToValue() {
        return toValue;
    }

    public void setToValue(String toValue) {
        this.toValue = toValue;
    }

    public Boolean getChanged() {
        return changed;
    }

    public void setChanged(Boolean changed) {
        this.changed = changed;
    }

    public Boolean getFlex() {
        return flex;
    }

    public void setFlex(Boolean flex) {
        this.flex = flex;
    }

    public Boolean getOverflowFlag() {
        return overflowFlag;
    }

    public void setOverflowFlag(Boolean overflowFlag) {
        this.overflowFlag = overflowFlag;
    }

    public String getMultilineHtmlFlag() {
        return multilineHtmlFlag;
    }

    public void setMultilineHtmlFlag(String multilineHtmlFlag) {
        this.multilineHtmlFlag = multilineHtmlFlag;
    }

    public String getObjectLabel() {
        return objectLabel;
    }

    public void setObjectLabel(String objectLabel) {
        this.objectLabel = objectLabel;
    }

    public String getFieldLabel() {
        return fieldLabel;
    }

    public void setFieldLabel(String fieldLabel) {
        this.fieldLabel = fieldLabel;
    }
}
