CREATE TABLE J_FORM_USAGES (
        FORM_NAME          VARCHAR(20) NOT NULL,
        EVENT_NAME          VARCHAR(20) NOT NULL,
        FORM_ALTERNATE          VARCHAR(20),
        COPIES          INT,
        CREATED_ON          DATETIME,
