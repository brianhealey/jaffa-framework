        LAST_CHANGED_ON          DATETIME,
        LAST_CHANGED_BY          VARCHAR(20),
    CONSTRAINT J_FORM_USAGESP1 PRIMARY KEY(FORM_NAME,EVENT_NAME)
) TYPE=InnoDB
